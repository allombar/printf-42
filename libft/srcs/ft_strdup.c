#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*cpy;
	int		i;

	cpy = (char *)malloc(sizeof(char) * ft_strlen(src) + 1);
	if (!cpy)
		return (NULL);
	i = 0;
	while (src[i])
	{
		cpy[i] = src[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
